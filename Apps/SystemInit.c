void InitDevices(void)
{
	cli();															// disable all interrupts

	sim_control.pInit();
	uart0_control.pInit();
	uart1_control.pInit();
	//uart1_control.pSetBaudRate(UART_BAUDRATE_2X, 57600L);
	timer0_control.pInit();
	timer3_control.pInit();
	adc_control.pInit();
	gpio_control.pInit();
	ppm_control.pInit();
	led_control.pInit();
	spi_control.pInit();
	extint_control.pInit();
	uart2_control.pInit();
	signal_control.pInit();

	//下面是小螃蟹写的
	//uart0_test.pInit();
	//上面是小螃蟹写的

	SetRegBit(DDRG, 3);
	SetRegBit(PORTG, 3);
	_delay_ms(1);
	ClrRegBit(PORTG,3);

	_delay_ms(100);													// 等待系统稳定

	sei();															// re-enable interrupts
}


